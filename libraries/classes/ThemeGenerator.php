/**
 * Holds the PhpMyAdmin\ThemeGenerator class
 *
 * @package PhpMyAdmin
 */
declare(strict_types=1);

namespace PhpMyAdmin;

use PhpMyAdmin\Theme\Navigation;
use PhpMyAdmin\Theme\Layout;
use PhpMyAdmin\Theme\Common;

/**
 * Set of functions for Automated Theme Generator in phpMyAdmin
 *
 * @package PhpMyAdmin
 */
class ThemeGenerator
{

    /**
     * Colour Picker HTML file
     *
     * @return string HTML for the color picker tool
     */
    public function colorPicker()
    {
        $output = '<div id="container">';
        $output .= '<div id="palette" class="block">';
        $output .= '<div id="color-palette"></div>';
        $output .= '</div>';
        $output .= '<div id="picker" class="block">';
        $output .= '<div class="ui-color-picker" data-topic="picker" data-mode="HSB"></div>';
        $output .= '<div id="picker-samples" sample-id="master"></div>';
        $output .= '</div>';
        $output .= '</div>';
        return $output;
    }

    /**
     * File creation form
     *
     * @return string HTML for the form submission
     */
    public function form()
    {
        $output = '<form action="index.php" method="post" id="save">';
        $output .= '<select name="type" id="theme">';
        $output .= '<option value="0">Triadic</option>';
        $output .= '<option value="1">Complementary</option>';
        $output .= '<option value="2">Adjacent</option>';
        $output .= '<option value="3">Monochrome</option>';
        $output .= '</select>';
        $output .= '<br>';
        $output .= '<input type="text" name="theme_name"></input>';
        $output .= '<br><br>';
        $output .= '<input type="submit">';
        $output .= '</form>';
        return $output;
    }

    /**
     * Creates file structure
     *
     * @param array $post POST form data
     *
     * @return null
     */
    public function createFileStructure(array $post)
    {
        $nav = new Navigation();
        $layout = new Layout();
        $common = new Common();
        $name = $post['theme_name'];
        if (!is_dir("themes/" . $name)) {
            mkdir("themes/" . $name);
            mkdir("themes/" . $name . "/css");
            $this->createJsonFile($name);
        }
        $common->createCommonFile($name);
        $layout->createLayoutFile($post);
        $nav->createNavigationFile($name);
        return null;
    }

    /**
     * Creates theme.json
     *
     * @param string $name name of new theme
     *
     * @return null
     */
    public function createJsonFile($name)
    {
        $file = fopen("themes/" . $name . "/theme.json", "w");
        $txt = '{';
        $txt .= '"name": "' . $name . '",';
        $txt .= '"version": "5.0",';
        $txt .= '"description": "Theme Generated by Automated Theme Generator",';
        $txt .= '"author": "phpMyAdmin developers",';
        $txt .= '"url": "https://www.phpmyadmin.net/",';
        $txt .= '"supports": ["5.0"]';
        $txt .= '}';
        fwrite($file, $txt);
        fclose($file);
        return null;
    }
}

